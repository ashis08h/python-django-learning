my_tuple = (1, 2, 3)
print(my_tuple)
print(type(my_tuple))

my_tuple = tuple([1, 2, 3])
print(my_tuple)
print(type(my_tuple))

my_tuple = 1, 2, 4
print(my_tuple)
print(type(my_tuple))

my_tuple = (1,)
print(my_tuple)
print(type(my_tuple))

empty_tuple = ()
print(empty_tuple)
print(type(empty_tuple))